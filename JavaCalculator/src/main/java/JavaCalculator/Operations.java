
package JavaCalculator;

/**
 * Class containing methods for mathematical operations
 * @author Hayden Eastwell
 */
public class Operations {
    
    /**
     * Method for addition operation
     * @param num First number in equation (double)
     * @param num2 Second number in equation (double)
     * @return Result of the operation
     */
    public static double add(double num, double num2) {
        return num + num2;
    } 
    
    /**
     * Method for subtraction operation
     * @param num First number in equation (double)
     * @param num2 Second number in equation (double)
     * @return Result of the operation
     */
    public static double subtract(double num, double num2) {
        return num - num2;
    }
    
    /**
     * Method for division operation
     * @param num First number in equation (double)
     * @param num2 Second number in equation (double)
     * @return Result of the operation
     */
    public static double divide(double num, double num2) {
        return num / num2;
    }
    
    /**
     * Method for multiplication operation
     * @param num First number in equation (double)
     * @param num2 Second number in equation (double)
     * @return Result of the operation
     */
    public static double multiply(double num, double num2) {
        return num * num2;
    }
    
}
